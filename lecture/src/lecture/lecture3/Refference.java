package lecture.lecture3;

public class Refference {

    private int a;
    private String s;

    public Refference(final int a, final String s) {
        this.a = a;
        this.s = s;
    }

    public int getA() {
        return a;
    }

    public String getS() {
        return s;
    }

    public void setA(final int a) {
        this.a = a;
    }

    public void setS(final String s) {
        this.s = s;
    }
}
